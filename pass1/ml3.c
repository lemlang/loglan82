void listing_error_line__( lun, line, nr, errno, nameid )
   word *lun,*line,*nr,*errno;
   char *nameid;
{
   FILE *f=stdout;
   if( *lun != 0L )  f=file_arr[*lun];
   fprintf(f,"*** %6d ERROR %6d %s %8.8s\n",(int)(*line),(int)(*nr),find_msg((int)(*errno)),nameid);
}



